% ======================================================================= %
% Use subplot to draw cos(nx) where n = 2, 6, 4, 8.
% ======================================================================= %
clc; clear; close all;

x = linspace(0, 10, 1000);
for n = 1:4
    subplot(2, 2, n);
    y = cos(2*n*x);
    plot(x, y)
end